// +, -, *, /, %, ++, --
var numOne = 1;
var numTwo = 2;
console.log("Addition:");
console.log(numOne + numTwo); // Addition
console.log("Subtraction:");
console.log(numOne - numTwo); // Subtraction
console.log("Multiplication:");
numOne = 10;
numTwo = 15;
console.log(numOne * numTwo); // Multiplication
console.log("Division:");
console.log(numOne / numTwo); // Division
console.log("Modulus:");
var numThree = 2;
var numFour = 21;
console.log(numFour % numThree); // Modulus
// Incrementor
console.log("Incrementor:");
var x1 = 0;
while (x1 < 10) {
    console.log(x1);
    x1++;
}
// Decrementor
console.log("Decrementor:");
var x2 = 10;
while (x2 > 0) {
    console.log(x2);
    x2--;
}
